        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> LogIn(string? email, string? password)
        {
            UsersDbContext? context = new UsersDbContext();
            UsersModel? user = await context.Users.FirstOrDefaultAsync(x => x.Email == email && x.Password == password);

            return user != null ? RedirectToAction("MainView", "Home") : View("Error", "Home"); 
        }

